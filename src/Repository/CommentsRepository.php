        return $this->createQueryBuilder('c')
            ->select('COUNT(c.id)')
            ->where("c.createdBy = :user_id")
            ->andWhere("c.writtenOn = :exam_id")
            ->setParameter("user_id", $user_id)
            ->setParameter("exam_id", $exam_id)
            ->getQuery()
            ->getSingleScalarResult();
